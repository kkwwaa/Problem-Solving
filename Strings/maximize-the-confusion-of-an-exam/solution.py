class Solution(object):
    def maxConsecutiveAnswers(self, s, k):
        # Инициализируем переменные:
        # maxf — максимальная частота символа в текущем окне
        # i — начало окна
        maxf = i = 0
        # count — словарь для подсчёта частоты символов ('T'/'F')
        count = Counter()

        # Перебираем конец окна (j) от 0 до длины строки
        for j in range(len(s)):
            # Увеличиваем частоту текущего символа s[j]
            count[s[j]] += 1
            # Обновляем maxf — максимальную частоту среди всех символов в окне
            maxf = max(maxf, count[s[j]])

            # Проверяем, является ли окно недопустимым:
            # Если длина окна (j - i + 1) больше, чем maxf + k,
            # значит, нужно заменить слишком много символов (> k)
            if j-i+1 > maxf+k:
                # Уменьшаем частоту символа в начале окна (s[i])
                count[s[i]] -= 1
                # Сдвигаем начало окна вправо
                i += 1
        # Возвращаем длину строки минус начало окна
        # Это максимальная длина допустимого окна
        return len(s)-i
        
