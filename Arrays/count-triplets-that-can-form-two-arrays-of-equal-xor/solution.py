class Solution(object):
        def countTriplets(self, arr):
            #вставляем 0 в начало массива для удобства вычисления префиксных XOR
            #теперь arr[0]=0, arr[1]=arr[0], arr[2]=arr[1] и тд
            arr.insert(0,0)

            #получаем длину массива после добавления 0
            n = len(arr)

            #преобразуем массив в массив префиксных XOR
            #после этого arr[i] = a[0]^arr[1]^...^arr[i-1]
            #arr = [0, 2, 3, 1] ---> arr = [0, 2, 2^3, 2^3^1]
            for i in range(n-1):
                arr[i+1] ^= arr[i]

            #переменная для подсчета количества триплетов
            res = 0

            #перебираем все пары (i, j), где i<j
            for i in range(n):
                for j in range(i+1, n):
                    #если arr[i]==arr[j], то XOR подмассива от i до j-1 равен 0
                    #это означает, что для любого k в диапазоне [i+1, j-1] мы получаем триплет
                    if arr[i] == arr[j]:
                        #количество подходящих k равно j-i-1
                        #например, если i=1, j=4, то k может быть 2 или 3 (4-1-1=2)
                        res += j-i-1

            #возвращаем общее количество триплетов
            return(res)
